package com.doubleu.login.vo;

public class LoginVo {
	int memberNo;
	String memberMid;
	String memberPwd;
	String memberName;
	String memberEmail;
	String memberDepartment;
	String memberTeam;
	String memberPhoneNumber;
	String memberZipcode;
	String memberAddress;
	String memberPosition;
	
	public int getMemberNo() {
		return memberNo;
	}
	public void setMemberNo(int memberNo) {
		this.memberNo = memberNo;
	}
	public String getMemberMid() {
		return memberMid;
	}
	public void setMemberMid(String memberMid) {
		this.memberMid = memberMid;
	}
	public String getMemberPwd() {
		return memberPwd;
	}
	public void setMemberPwd(String memberPwd) {
		this.memberPwd = memberPwd;
	}
	public String getMemberName() {
		return memberName;
	}
	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}
	public String getMemberEmail() {
		return memberEmail;
	}
	public void setMemberEmail(String memberEmail) {
		this.memberEmail = memberEmail;
	}
	public String getMemberDepartment() {
		return memberDepartment;
	}
	public void setMemberDepartment(String memberDepartment) {
		this.memberDepartment = memberDepartment;
	}
	public String getMemberTeam() {
		return memberTeam;
	}
	public void setMemberTeam(String memberTeam) {
		this.memberTeam = memberTeam;
	}
	public String getMemberPhoneNumber() {
		return memberPhoneNumber;
	}
	public void setMemberPhoneNumber(String memberPhoneNumber) {
		this.memberPhoneNumber = memberPhoneNumber;
	}
	public String getMemberZipcode() {
		return memberZipcode;
	}
	public void setMemberZipcode(String memberZipcode) {
		this.memberZipcode = memberZipcode;
	}
	public String getMemberAddress() {
		return memberAddress;
	}
	public void setMemberAddress(String memberAddress) {
		this.memberAddress = memberAddress;
	}
	public String getMemberPosition() {
		return memberPosition;
	}
	public void setMemberPosition(String memberPosition) {
		this.memberPosition = memberPosition;
	}
	
	@Override
	public String toString() {
		return "LoginVo [memberNo=" + memberNo + ", memberMid=" + memberMid + ", memberPwd=" + memberPwd
				+ ", memberName=" + memberName + ", memberEmail=" + memberEmail + ", memberDepartment="
				+ memberDepartment + ", memberTeam=" + memberTeam + ", memberPhoneNumber=" + memberPhoneNumber
				+ ", memberZipcode=" + memberZipcode + ", memberAddress=" + memberAddress + ", memberPosition="
				+ memberPosition + "]";
	}
	
	
}
