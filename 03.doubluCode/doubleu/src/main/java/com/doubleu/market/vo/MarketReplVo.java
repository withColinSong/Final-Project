package com.doubleu.market.vo;

public class MarketReplVo {
	int replNo;
	int marketNo;
	String replWriter;
	String replDoc;
	String replDate;
	public int getReplNo() {
		return replNo;
	}
	public void setReplNo(int replNo) {
		this.replNo = replNo;
	}
	public int getMarketNo() {
		return marketNo;
	}
	public void setMarketNo(int marketNo) {
		this.marketNo = marketNo;
	}
	public String getReplWriter() {
		return replWriter;
	}
	public void setReplWriter(String replWriter) {
		this.replWriter = replWriter;
	}
	public String getReplDoc() {
		return replDoc;
	}
	public void setReplDoc(String replDoc) {
		this.replDoc = replDoc;
	}
	public String getReplDate() {
		return replDate;
	}
	public void setReplDate(String replDate) {
		this.replDate = replDate;
	}
	
	
}
