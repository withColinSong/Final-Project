package com.doubleu.profile.vo;

public class WorkVo {
	int workNo;
	int memberNo;
	String workMid;
	String workDate;
	String workStart;
	String workFinish;
	String workTotal;
	String workPlus;
	public int getWorkNo() {
		return workNo;
	}
	public void setWorkNo(int workNo) {
		this.workNo = workNo;
	}
	public int getMemberNo() {
		return memberNo;
	}
	public void setMemberNo(int memberNo) {
		this.memberNo = memberNo;
	}
	public String getWorkMid() {
		return workMid;
	}
	public void setWorkMid(String workMid) {
		this.workMid = workMid;
	}
	public String getWorkDate() {
		return workDate;
	}
	public void setWorkDate(String workDate) {
		this.workDate = workDate;
	}
	public String getWorkStart() {
		return workStart;
	}
	public void setWorkStart(String workStart) {
		this.workStart = workStart;
	}
	public String getWorkFinish() {
		return workFinish;
	}
	public void setWorkFinish(String workFinish) {
		this.workFinish = workFinish;
	}
	public String getWorkTotal() {
		return workTotal;
	}
	public void setWorkTotal(String workTotal) {
		this.workTotal = workTotal;
	}
	public String getWorkPlus() {
		return workPlus;
	}
	public void setWorkPlus(String workPlus) {
		this.workPlus = workPlus;
	}
	
}
